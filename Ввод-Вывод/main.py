# print() - функция для вывода информации на экран
print('Введите число: ')

# input() - функция, получающая строку с клавиатуры
# int() - функция, преобразующая строку в целое число
num = int(input())

# f' {}' форматированный вывод
# Удобен, когда в одной строке надо вывести и строку и переменные
print(f'Вы ввели число {num}')

